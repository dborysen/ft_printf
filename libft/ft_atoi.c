int	ft_atoi(const char *str)
{
	int				i;
	int				k;
	long long int	num;

	k = 1;
	i = 0;
	num = 0;
	while (str[i] == ' ' || str[i] == '\t' || str[i] == '\n' ||\
str[i] == '\r' || str[i] == '\v' || str[i] == '\f')
		i++;
	if (str[i] == '-' && str[i + 1] >= 48 && str[i++ + 1] <= 57)
		k = -1;
	if (str[i] == '+')
		i++;
	while (str[i] >= 48 && str[i] <= 57)
	{
		if ((num > 922337203685477580 || (num == \
922337203685477580 && (str[i] - '0') > 7)) && k == 1)
			return (-1);
		else if ((num > 922337203685477580 || (num == \
922337203685477580 && (str[i] - '0') > 8)) && k == -1)
			return (0);
		num = num * 10 + (str[i++] - '0');
	}
	return (num * k);
}
